// Menggunakan var
var x = 10;
document.write("<p>Nilai x (dengan var): " + x + "</p>");

// Menggunakan let
let y = 20;
document.write("<p>Nilai y (dengan let): " + y + "</p>");

// Menggunakan const
const z = 30;
document.write("<p>Nilai z (dengan const): " + z + "</p>");
 
// Tipe Data String
let nama = "Muhammad Nizar";
console.log("Nama:", nama);
alert("Nama: " + nama);

// Tipe Data Object
let mahasiswa = {
   nama: "Nizar",
   jurusan: "Sistem Informasi",
   universitas: "Universitas Negeri Surabaya"
};
console.log("Data Mahasiswa:", mahasiswa);
alert("Data Mahasiswa: Nama - " + mahasiswa.nama + ", Jurusan - " + mahasiswa.jurusan + ", Universitas - " + mahasiswa.universitas);